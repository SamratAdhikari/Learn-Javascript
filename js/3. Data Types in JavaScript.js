// Primitive Data Types:

// String
let name = "samrat";
console.log("My name is " + name);
console.log("Data Type is: " + (typeof name));

// Numbers
let marks = 100;
console.log("My marks is " + marks);
console.log("Data Type is " + (typeof marks))

// Boolean
let driver = true;
console.log("Am I a driver? " + driver)
console.log("Data Type is " + (typeof driver))

// Null
let trophies = null;
console.log("How many trophies do Spurs have? " + trophies)
console.log("Data Type is " + (typeof trophies))

// Undefined
let retire = undefined;
console.log("When will Buffon retire? " + retire)
console.log("Data Type is " + (typeof retire))






// Reference Data Types

// Arrays
arr = [1, 2.2, "Hi", true]
console.log("Collection of numbers, string and boolean var: " + arr)
console.log("Data Type is " + (typeof arr))


// Object Literals
let student_marks = {
    samrat : 100,
    naruto : 80,
    "d. luffy"  : 70
}
console.log("Student Marks: " + student_marks)
console.log("Data Type is " + (typeof student_marks))

// Functions
function findName() {
}
console.log("Data Type is " + typeof findName)

// Date 
let date = new Date();
console.log("Data Type of date is " + (typeof date))































